export function buildStarterSitePatch(repoName: string): RepoPatch {
  const title = titleFromRepoName(repoName) || "Starter Site";

  return {
    commitMessage: "Initialize Vibex starter site",
    summary: `Seed a minimal Next.js App Router starter for ${title}.`,
    files: [
      {
        path: "package.json",
        operation: "upsert",
        reason: "Create a lightweight Next.js and Tailwind starter workspace.",
        content: buildPackageJson(title)
      },
      {
        path: "tsconfig.json",
        operation: "upsert",
        reason: "Add a TypeScript config for the starter app.",
        content: `{
  "compilerOptions": {
    "target": "ES2017",
    "lib": ["dom", "dom.iterable", "esnext"],
    "allowJs": false,
    "skipLibCheck": true,
    "strict": true,
    "noEmit": true,
    "esModuleInterop": true,
    "module": "esnext",
    "moduleResolution": "bundler",
    "resolveJsonModule": true,
    "isolatedModules": true,
    "jsx": "preserve",
    "incremental": true,
    "plugins": [{ "name": "next" }]
  },
  "include": ["next-env.d.ts", "**/*.ts", "**/*.tsx", ".next/types/**/*.ts"],
  "exclude": ["node_modules"]
}
`
      },
      {
        path: "next-env.d.ts",
        operation: "upsert",
        reason: "Provide Next.js TypeScript environment declarations.",
        content: `/// <reference types="next" />
/// <reference types="next/image-types/global" />

// This file is automatically generated by Next.js.
`
      },
      {
        path: "next.config.ts",
        operation: "upsert",
        reason: "Enable a typed Next.js config file.",
        content: `import type { NextConfig } from "next";

const nextConfig: NextConfig = {};

export default nextConfig;
`
      },
      {
        path: "postcss.config.mjs",
        operation: "upsert",
        reason: "Configure Tailwind CSS processing.",
        content: `export default {
  plugins: {
    tailwindcss: {},
    autoprefixer: {}
  }
};
`
      },
      {
        path: "tailwind.config.ts",
        operation: "upsert",
        reason: "Set up Tailwind content scanning for the starter app.",
        content: `import type { Config } from "tailwindcss";

const config: Config = {
  content: ["./app/**/*.{ts,tsx}", "./components/**/*.{ts,tsx}"],
  theme: {
    extend: {}
  },
  plugins: []
};

export default config;
`
      },
      {
        path: "app/layout.tsx",
        operation: "upsert",
        reason: "Create the App Router root layout.",
        content: `import type { Metadata } from "next";

import "./globals.css";

export const metadata: Metadata = {
  title: "${title}",
  description: "A Vibex starter site ready for real-time design changes."
};

export default function RootLayout({
  children
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <body>{children}</body>
    </html>
  );
}
`
      },
      {
        path: "app/page.tsx",
        operation: "upsert",
        reason: "Ship an initial landing page that is easy to iterate on.",
        content: buildPageSource(title)
      },
      {
        path: "app/globals.css",
        operation: "upsert",
        reason: "Add the Tailwind base layer and starter design tokens.",
        content: `@tailwind base;
@tailwind components;
@tailwind utilities;

:root {
  color-scheme: dark;
}

* {
  box-sizing: border-box;
}

html,
body {
  margin: 0;
  min-height: 100%;
  background: #05070c;
  color: #f4f7fb;
  font-family: Inter, ui-sans-serif, system-ui, -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif;
}

a {
  color: inherit;
  text-decoration: none;
}
`
      },
      {
        path: "README.md",
        operation: "upsert",
        reason: "Explain how to run the seeded starter locally.",
        content: `# ${title}

This repository was initialized by Vibex with a minimal Next.js App Router and Tailwind starter.

## Run locally

\`\`\`bash
npm install
npm run dev
\`\`\`

Connect the repository to Vercel when you are ready to preview live deployments.
`
      }
    ]
  };
}
