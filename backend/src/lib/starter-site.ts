import type { RepoPatch } from "@/contracts";

function titleFromRepoName(repoName: string) {
  return repoName
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((part) => part[0]?.toUpperCase() + part.slice(1))
    .join(" ");
}

function buildPackageJson(title: string) {
  return JSON.stringify(
    {
      name: title.toLowerCase().replace(/\s+/g, "-"),
      private: true,
      scripts: {
        dev: "next dev",
        build: "next build",
        start: "next start",
        lint: "next lint"
      },
      dependencies: {
        next: "latest",
        react: "latest",
        "react-dom": "latest"
      },
      devDependencies: {
        "@types/node": "latest",
        "@types/react": "latest",
        "@types/react-dom": "latest",
        autoprefixer: "latest",
        postcss: "latest",
        tailwindcss: "latest",
        typescript: "latest"
      }
    },
    null,
    2
  );
}

